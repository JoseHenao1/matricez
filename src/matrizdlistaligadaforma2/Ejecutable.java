    private void MatrizCargar(String entrada){
        String filass,columnass;
        int cFilas,cColumnas;
        String[] lineas = entrada.split("\n");
        filass = lineas[0].trim();
        columnass = lineas[1].trim();
        filas=Integer.parseInt(filass);
        columnas=Integer.parseInt(columnass);
        obj = new MatrizListaligadaforma2(filas,columnas);
        for (int i = 2; i <lineas.length; i++) {
            String []colaboradorT,temp,temporal;
            
            temp=lineas[i].split("-");
            colaboradorT=new String[temp.length+1];
            temporal=temp[1].split("=");
            
            for (int j = 0; j <temp.length; j++) {
                colaboradorT[j]=temp[j].trim();
                
            }
            
            colaboradorT[1]=temporal[0];
            colaboradorT[2]=temporal[1];
            int []colaborador=new int[colaboradorT.length];
            char problematico=colaboradorT[2].charAt(0);
            colaborador[0]=Integer.parseInt(colaboradorT[0]);
            colaborador[1]=Integer.parseInt(colaboradorT[1]);
            colaborador[2]=Integer.parseInt(Character.toString(problematico)); 
            CrearMatriz(colaborador[0], colaborador[1],colaborador[2]);
            //obj.setCelda(colaborador[0], colaborador[1],colaborador[2]);
        }
    }
    
    private void CrearMatriz(int filaInicial,int filaFinal,int valor){
        int z=filas-1;
        int w,x,y=0;
        for (int i = filaInicial; i <= filaFinal; i++) {
            w=i;
            if (i>filas) {
                w=i%filas;
                if (w==0) {
                    w=filas;
                }
            }
            x=filas-w;
            if (z!=0) {
                y=columnas-1;
                z--;
            }else{
                z=filas-1;
                y=columnas-1;
                columnas--;
            }
            
            obj.setCelda(x,y,valor);
        }
    }
